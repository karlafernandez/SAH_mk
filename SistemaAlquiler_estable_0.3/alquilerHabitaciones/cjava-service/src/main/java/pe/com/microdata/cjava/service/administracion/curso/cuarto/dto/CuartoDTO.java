package pe.com.microdata.cjava.service.administracion.curso.cuarto.dto;

import java.util.Date;

public class CuartoDTO {
    
    private Integer idCuarto;
    private String direccion;
    private String nomenCuarto;
    private Integer numAmbientes;
    private Date fechaRegistro;
    private Integer activacion;
    private String restricciones;
    private String detalle;
    private Integer ubigeo;
    private Integer arrendatario;

    /**
     * @return the idCuarto
     */
    public Integer getIdCuarto() {
        return idCuarto;
    }

    /**
     * @param idCuarto the idCuarto to set
     */
    public void setIdCuarto(Integer idCuarto) {
        this.idCuarto = idCuarto;
    }

    /**
     * @return the direccion
     */
    public String getDireccion() {
        return direccion;
    }

    /**
     * @param direccion the direccion to set
     */
    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    /**
     * @return the nomenCuarto
     */
    public String getNomenCuarto() {
        return nomenCuarto;
    }

    /**
     * @param nomenCuarto the nomenCuarto to set
     */
    public void setNomenCuarto(String nomenCuarto) {
        this.nomenCuarto = nomenCuarto;
    }

    /**
     * @return the numAmbientes
     */
    public Integer getNumAmbientes() {
        return numAmbientes;
    }

    /**
     * @param numAmbientes the numAmbientes to set
     */
    public void setNumAmbientes(Integer numAmbientes) {
        this.numAmbientes = numAmbientes;
    }

    /**
     * @return the fechaRegistro
     */
    public Date getFechaRegistro() {
        return fechaRegistro;
    }

    /**
     * @param fechaRegistro the fechaRegistro to set
     */
    public void setFechaRegistro(Date fechaRegistro) {
        this.fechaRegistro = fechaRegistro;
    }

    /**
     * @return the activacion
     */
    public Integer getActivacion() {
        return activacion;
    }

    /**
     * @param activacion the activacion to set
     */
    public void setActivacion(Integer activacion) {
        this.activacion = activacion;
    }

    /**
     * @return the restricciones
     */
    public String getRestricciones() {
        return restricciones;
    }

    /**
     * @param restricciones the restricciones to set
     */
    public void setRestricciones(String restricciones) {
        this.restricciones = restricciones;
    }

    /**
     * @return the detalle
     */
    public String getDetalle() {
        return detalle;
    }

    /**
     * @param detalle the detalle to set
     */
    public void setDetalle(String detalle) {
        this.detalle = detalle;
    }

    /**
     * @return the ubigeo
     */
    public Integer getUbigeo() {
        return ubigeo;
    }

    /**
     * @param ubigeo the ubigeo to set
     */
    public void setUbigeo(Integer ubigeo) {
        this.ubigeo = ubigeo;
    }

    /**
     * @return the arrendatario
     */
    public Integer getArrendatario() {
        return arrendatario;
    }

    /**
     * @param arrendatario the arrendatario to set
     */
    public void setArrendatario(Integer arrendatario) {
        this.arrendatario = arrendatario;
    }
    
      
   
   
            
}
